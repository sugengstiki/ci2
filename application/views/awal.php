	<h3>SMATIKA Jurnal Vol 02, No 01 (2012)</h3>
			<p></p><p>STIKI Informatika Jurnal (SMATIKA Jurnal) merupakan jurnal 
yang diterbitkan oleh Lembaga Penelitian &amp; Pengabdian kepada 
Masyarakat (LPPM), Sekolah Tinggi Informatika &amp; Komputer Indonesia 
(STIKI) Malang.</p><p>Pada Edisi ini, SMATIKA Jurnal menyajikan 5 (lima)
 naskah dalam bidang sistem informasi, database dan sebagainya. Redaksi 
mengucapkan terima kasih dan selamat kepada Pemakalah yang diterima dan 
diterbitkan dalam edisi ini, karena telah memberikan kontribusi penting 
dalam pengembangan ilmu dan teknologi.</p><p>Sejumlah pakar dari luar 
STIKI (Mitra Bestari) telah memberikan kontribusinya yang sangat 
berharga dalam menilai naskah yang dimuat. Untuk itu redaksi 
menyampaikan banyak terima kasih kepada para Penilai (Reviewer) 
tersebut.</p><p></p>
	
