	<h3>Prosiding SNATIKA Vol 01 (2011)</h3>
			<p>SNATIKA 2011 adalah Seminar Nasional Teknologi Informasi, 
Komunikasi dan Aplikasinya yang diselenggarakan oleh STIKI Malang 
bekerjasama dengan APTIKOM Pusat dalam acara RAKORNAS APTIKOM 2011 yang 
pelaksanaannya diadakan di Batu, Malang. Sesuai tujuannya SNATIKA 2011 
merupakan sarana bagi peneliti, akademisi dan praktisi untuk 
mempublikasikan hasil-hasil penelitian, ide-ide terbaru mengenai 
Teknologi Informasi, Komunikasi dan Aplikasinya. Selain itu sesuai 
dengan tema Rakornas APTIKOM 2011 "<em>Synchronising Motions For Actions</em>",
 topik-topik yang diambil disesuaikan dengan kompetensi dasar dari 
APTIKOM yang diharapkan dapatt mensinergikan penelitian yang dilakukan 
oleh para peneliti di bidang informatika dan komputer. Semoga bermanfaat
 bagi kita semua terutama bagi perkembangan ilmu dan teknologi di bidang
 teknologi informasi, komunikasi dan aplikasinya.</p>
	
